import express from "express";
import {
  sendMailToProfessors,
  sendMailToStudents,
  sendMailToRecruiters,
  fetchMails,
} from "../controller/mailbox.js";

const router = express.Router();

// Mail routes
router.post("/send-to-professors", sendMailToProfessors);
router.post("/send-to-students", sendMailToStudents);
router.post("/send-to-recruiters", sendMailToRecruiters);
router.get("/fetch/:email", fetchMails);

export default router;